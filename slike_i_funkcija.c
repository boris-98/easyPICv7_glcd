//definicija
void GLCD_partialDisplayPicture (unsigned char *slika)
{

unsigned char q,vert;

	for (vert = 6; vert< 8; vert++) /* loop on the 2 upper pages */
    	{
      		GoToXY(0,vert);
      		for (q = 0; q < 64; q++)
				{
				LcdDataWrite(slika[vert*128+q]);
				}
			GoToXY(64,vert);
      		for (q = 0; q < 64; q++)
				{
				LcdDataWrite(slika[vert*128+q+64]);
				}
		}
}

